  async postAddProduct(req, res) {
    let { pName, pDescription, pPrice, pQuantity, pCategory, pOffer, pStatus } =
      req.body;
    let images = req.files;
    // Validation
    if (
      !pName |
      !pDescription |
      !pPrice |
      !pQuantity |
      !pCategory |
      !pOffer |
      !pStatus
    ) {
      Product.deleteImages(images, "file");
      return res.json({ error: "All required fields must be filled" });
    }
    // Validate Name and description
    else if (pName.length > 255 || pDescription.length > 3000) {
      Product.deleteImages(images, "file");
      return res.json({
        error: "Name 255 & Description must not be 3000 character long",
      });
    }
    // Validate Images
    else if (images.length !== 2) {
      Product.deleteImages(images, "file");
      return res.json({ error: "Provide at least 2 images" });
    } else {
      try {
        let allImages = [];
        for (const img of images) {
          allImages.push(img.filename);
        }
        let newProduct = await models.products.create({
          pImages: JSON.stringify(allImages),
          pName: pName,
          pDescription: pDescription,
          pPrice: pPrice,
          pQuantity: pQuantity,
          pCategory: pCategory,
          pOffer: pOffer,
          pStatus: pStatus,
        });
        return res.json({ success: "Product created successfully" });
      } catch (err) {
        console.log(err);
      }
    }
  }

